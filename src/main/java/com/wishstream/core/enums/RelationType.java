package com.wishstream.core.enums;

public enum RelationType {
    FAMILY,            // Immediate family members
    BUSINESS,          // Business relationships
    BLOOD_RELATIVE,    // Extended family related by blood
    COLLEAGUE,         // Work colleagues
    FRIEND,            // Personal friends
    NEIGHBOR,          // People living nearby
    PARTNER,           // Romantic partners
    EX,                // Ex-partners
    TEACHER,           // Teachers or mentors
    STUDENT,           // Students of the user
    CLIENT,            // Professional clients
    VENDOR,            // Service or product vendors
    OTHER              // Catch-all for undefined relations
}
