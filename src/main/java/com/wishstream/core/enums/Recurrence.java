package com.wishstream.core.enums;

public enum Recurrence {
    NONE,              // No recurrence
    DAILY,             // Every day
    WEEKLY,            // Every week
    BIWEEKLY,          // Every two weeks
    MONTHLY,           // Every month
    BIMONTHLY,         // Every two months
    QUARTERLY,         // Every three months
    HALF_YEARLY,       // Every six months
    YEARLY,            // Every year
    CUSTOM             // User-defined recurrence (handled separately)
}
